// функции every и some 

// для начала нужо создать собственную 
// функцию со своим названием
function elLength(el) {
    return el.length == 3;
}
// например если длина элемента буде 3 
// будет возвращаться true , в других 
// случаях - false;
console.log(arr.every(elLength));
// false - каждый элемент должен соответствовать
console.log(arr.some(elLength));
// true - хотяб один должен соответствовать


// Функция reduce
let nums = [1, 2, 3, 4, 5, 2, 23];
// посчитаем сумму всех элементов
let summa = nums.reduce(function(sum, el){
    return sum + el;
});
// Функция редюс позволяет последовательно 
// обработать все элементы масива;
console.log(summa);
// 40
// Функция, выполняющаяся для каждого элемента 
// массива принимает четыре аргумента:
