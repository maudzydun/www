// Перебор масивов при помощи функций
var arr = ['BMW', 'VW', 'Audi', 'Ford'];

// при помощи forEach
arr.forEach(function(item, i, array){
    console.log('Элемент №' + i + ': ' + item + '. ' + array);
});


// при помощи filter
let newArr = arr.filter(function(el){
    return el.length == 2 || el.length == 4;
});
// результатом выполнения этой функции будет создан новый масив
// елементов которые подходили под определенный фильтр
// внутри фанкшин указываем параметр - тот элемент с которым мы
// работаем, а внутри фигурных скобок прописываем слово return -
// будем возвращать элементы которые подходят под проверку.
// Конкретно здесь мы создаем новый массив , при этом если у нас
// длина элемента из масива arr будет равна 2 или равна 4. То
// есть в новый масив будет добавляться только такие элементы.
console.log(newArr);
// (3) ['VW', 'Audi', 'Ford']

let arr_2 = [0, 2.5, 5, 9.54, 23, 53, 54.6];
let Arr_2 = arr_2.filter(function(el){
    return el % 1 == 0;
});
console.log(Arr_2);
// (4) [0, 5, 23, 53] к примеру вывели только целые числа


// при помощи map
// служит для перебора масива с целью 
// изменения каждого обьекта
let lower_arr = arr.map(function(el){
    return el.toLocaleLowerCase();
});
// будем каждый элемент переводить в 
// нижний регистр
console.log(lower_arr);
// (4) ['bmw', 'vw', 'audi', 'ford'];
